function resizeCanvas() {
  if (!canvas || !ctx) return;

  const ratio = isMobileViewport() ? 1 : (window.devicePixelRatio || 1);
  canvas.width = Math.floor(window.innerWidth * ratio);
  canvas.height = Math.floor(window.innerHeight * ratio);
  canvas.style.width = `${window.innerWidth}px`;
  canvas.style.height = `${window.innerHeight}px`;
  ctx.setTransform(ratio, 0, 0, ratio, 0, 0);

  const particleCount = isMobileViewport() ? Math.min(12, Math.floor(window.innerWidth / 42)) : Math.min(48, Math.floor(window.innerWidth / 24));
  particles = Array.from({ length: particleCount }, () => ({
    x: Math.random() * window.innerWidth,
    y: Math.random() * window.innerHeight,
    size: 1 + Math.random() * 3,
    speed: 0.18 + Math.random() * 0.36,
    hue: Math.random()
  }));
}

function drawAmbient() {
  if (!canvas || !ctx) return;
  if (document.hidden) {
    requestAnimationFrame(drawAmbient);
    return;
  }

  ctx.clearRect(0, 0, window.innerWidth, window.innerHeight);
  const styles = getComputedStyle(document.body);
  const accent = styles.getPropertyValue("--accent").trim();
  const accentTwo = styles.getPropertyValue("--accent-2").trim();

  particles.forEach((particle) => {
    particle.x += (pointer.x - 0.5) * particle.speed;
    particle.y -= particle.speed;

    if (particle.y < -12) {
      particle.y = window.innerHeight + 12;
      particle.x = Math.random() * window.innerWidth;
    }

    ctx.beginPath();
    ctx.fillStyle = particle.hue > 0.55 ? accent : accentTwo;
    ctx.globalAlpha = 0.16;
    ctx.rect(particle.x, particle.y, particle.size * 7, particle.size);
    ctx.fill();
  });

  ctx.globalAlpha = 1;
  requestAnimationFrame(drawAmbient);
}

function isImageHref(href) {
  return /\.(png|jpe?g)$/i.test(href || "");
}

function imageLinkCaption(link) {
  if (link.classList.contains("timeline-card")) {
    const title = link.querySelector("strong")?.textContent.trim() || "";
    const prize = link.querySelector("small")?.textContent.trim() || "";
    return [title, prize].filter(Boolean).join(" / ");
  }

  return link.textContent.trim();
}

function openLinkedImage(link) {
  const group = link.closest(".timeline-grid, .resume-list, .profile-meta");
  const links = group
    ? [...group.querySelectorAll("a[href]")].filter((item) => isImageHref(item.getAttribute("href")))
    : [link];
  const items = links.map((item) => ({
    src: item.getAttribute("href"),
    caption: imageLinkCaption(item)
  }));
  const index = Math.max(links.indexOf(link), 0);

  openImageViewer(link.getAttribute("href"), imageLinkCaption(link), items, index);
}

