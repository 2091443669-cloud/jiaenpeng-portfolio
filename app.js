class AwardsLightRays {
  constructor(mount) {
    this.mount = mount;
    this.section = mount.closest(".awards-section");
    this.mouse = { x: 0.5, y: 0.5 };
    this.smoothMouse = { x: 0.5, y: 0.5 };
    this.isVisible = true;
    this.raf = null;

    this.canvas = document.createElement("canvas");
    this.canvas.setAttribute("aria-hidden", "true");
    this.mount.replaceChildren(this.canvas);
    this.gl = this.canvas.getContext("webgl", { alpha: true, antialias: true, premultipliedAlpha: false });

    if (!this.gl || !this.createProgram()) {
      this.mount.classList.add("is-fallback");
      return;
    }

    this.onResize = this.onResize.bind(this);
    this.onPointerMove = this.onPointerMove.bind(this);
    this.onVisibilityChange = this.onVisibilityChange.bind(this);
    this.onIntersection = this.onIntersection.bind(this);
    this.frame = this.frame.bind(this);

    this.resizeObserver = typeof ResizeObserver === "undefined" ? null : new ResizeObserver(this.onResize);
    this.resizeObserver?.observe(this.mount);
    this.intersectionObserver = typeof IntersectionObserver === "undefined"
      ? null
      : new IntersectionObserver(this.onIntersection, { threshold: 0.08 });
    this.intersectionObserver?.observe(this.section || this.mount);

    this.section?.addEventListener("pointermove", this.onPointerMove, { passive: true });
    document.addEventListener("visibilitychange", this.onVisibilityChange);
    window.addEventListener("resize", this.onResize, { passive: true });

    this.onResize();
    this.start();
  }

  createProgram() {
    const vertex = `
      attribute vec2 aPosition;
      void main() {
        gl_Position = vec4(aPosition, 0.0, 1.0);
      }
    `;
    const fragment = `
      precision highp float;

      uniform float uTime;
      uniform vec2 uResolution;
      uniform vec2 uMouse;
      uniform float uMouseInfluence;

      float rayStrength(vec2 source, vec2 rayDirection, vec2 point, float seedA, float seedB) {
        vec2 sourceToPoint = point - source;
        float distance = length(sourceToPoint);
        vec2 direction = sourceToPoint / max(distance, 0.0001);
        float alignment = max(dot(direction, rayDirection), 0.0);
        float spread = pow(alignment, 2.0);
        float wave =
          0.50 + 0.17 * sin(alignment * seedA + uTime * 1.5) +
          0.20 + 0.13 * cos(alignment * seedB - uTime * 1.1);
        float distanceFade = clamp((uResolution.x * 3.0 - distance) / (uResolution.x * 3.0), 0.0, 1.0);
        float originFade = clamp((uResolution.x - distance) / uResolution.x, 0.45, 1.0);
        return max(wave, 0.0) * spread * distanceFade * originFade;
      }

      void main() {
        // Work in top-left coordinates so the source is above the section.
        vec2 point = vec2(gl_FragCoord.x, uResolution.y - gl_FragCoord.y);
        vec2 source = vec2(uResolution.x * 0.5, -uResolution.y * 0.20);
        vec2 baseDirection = vec2(0.0, 1.0);
        vec2 mouseDirection = normalize(uMouse * uResolution - source);
        vec2 direction = normalize(mix(baseDirection, mouseDirection, uMouseInfluence));
        float rays = rayStrength(source, direction, point, 36.2214, 21.11349) * 0.52;
        rays += rayStrength(source, direction, point, 22.3991, 18.0234) * 0.38;
        float topFade = 1.0 - point.y / uResolution.y;
        float intensity = rays * (0.56 + topFade * 0.70);
        vec3 lowerBlue = vec3(0.455, 0.647, 0.882);
        vec3 upperLavender = vec3(0.769, 0.694, 0.906);
        vec3 color = mix(lowerBlue, upperLavender, topFade) * intensity;
        gl_FragColor = vec4(color, clamp(intensity * 1.15, 0.0, 0.68));
      }
    `;
    const compile = (type, source) => {
      const shader = this.gl.createShader(type);
      this.gl.shaderSource(shader, source);
      this.gl.compileShader(shader);
      if (this.gl.getShaderParameter(shader, this.gl.COMPILE_STATUS)) return shader;
      this.gl.deleteShader(shader);
      return null;
    };
    const vertexShader = compile(this.gl.VERTEX_SHADER, vertex);
    const fragmentShader = compile(this.gl.FRAGMENT_SHADER, fragment);
    if (!vertexShader || !fragmentShader) return false;

    this.program = this.gl.createProgram();
    this.gl.attachShader(this.program, vertexShader);
    this.gl.attachShader(this.program, fragmentShader);
    this.gl.linkProgram(this.program);
    if (!this.gl.getProgramParameter(this.program, this.gl.LINK_STATUS)) return false;

    const buffer = this.gl.createBuffer();
    this.gl.bindBuffer(this.gl.ARRAY_BUFFER, buffer);
    this.gl.bufferData(this.gl.ARRAY_BUFFER, new Float32Array([-1, -1, 3, -1, -1, 3]), this.gl.STATIC_DRAW);
    this.gl.useProgram(this.program);
    const position = this.gl.getAttribLocation(this.program, "aPosition");
    this.gl.enableVertexAttribArray(position);
    this.gl.vertexAttribPointer(position, 2, this.gl.FLOAT, false, 0, 0);
    this.uniforms = {
      time: this.gl.getUniformLocation(this.program, "uTime"),
      resolution: this.gl.getUniformLocation(this.program, "uResolution"),
      mouse: this.gl.getUniformLocation(this.program, "uMouse"),
      mouseInfluence: this.gl.getUniformLocation(this.program, "uMouseInfluence")
    };
    this.gl.uniform1f(this.uniforms.mouseInfluence, 0.1);
    return true;
  }

  onResize() {
    if (!this.gl) return;
    const width = Math.max(1, this.mount.clientWidth);
    const height = Math.max(1, this.mount.clientHeight);
    const dpr = Math.min(window.devicePixelRatio || 1, 1.5);
    this.canvas.width = Math.round(width * dpr);
    this.canvas.height = Math.round(height * dpr);
    this.gl.viewport(0, 0, this.canvas.width, this.canvas.height);
    this.gl.useProgram(this.program);
    this.gl.uniform2f(this.uniforms.resolution, this.canvas.width, this.canvas.height);
  }

  onPointerMove(event) {
    const rect = this.mount.getBoundingClientRect();
    if (!rect.width || !rect.height) return;
    this.mouse.x = Math.min(1, Math.max(0, (event.clientX - rect.left) / rect.width));
    this.mouse.y = Math.min(1, Math.max(0, (event.clientY - rect.top) / rect.height));
  }

  onIntersection(entries) {
    this.isVisible = Boolean(entries[0]?.isIntersecting);
    if (this.isVisible) this.start();
  }

  onVisibilityChange() {
    if (!document.hidden) this.start();
  }

  start() {
    if (!this.gl || this.raf || !this.isVisible || document.hidden) return;
    this.raf = window.requestAnimationFrame(this.frame);
  }

  frame(timestamp) {
    this.raf = null;
    if (!this.gl || !this.isVisible || document.hidden) return;
    const smoothing = 0.92;
    this.smoothMouse.x = this.smoothMouse.x * smoothing + this.mouse.x * (1 - smoothing);
    this.smoothMouse.y = this.smoothMouse.y * smoothing + this.mouse.y * (1 - smoothing);
    this.gl.useProgram(this.program);
    this.gl.uniform1f(this.uniforms.time, timestamp * 0.001);
    this.gl.uniform2f(this.uniforms.mouse, this.smoothMouse.x, this.smoothMouse.y);
    this.gl.clearColor(0, 0, 0, 0);
    this.gl.clear(this.gl.COLOR_BUFFER_BIT);
    this.gl.drawArrays(this.gl.TRIANGLES, 0, 3);
    this.raf = window.requestAnimationFrame(this.frame);
  }
}

