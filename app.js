function lockPageScroll() {
  if (scrollLockDepth === 0) {
    if (isMobileViewport()) {
      scrollLockMode = "fixed";
      lockedScrollY = window.scrollY || window.pageYOffset || 0;
      document.documentElement.classList.add("is-scroll-locked");
      document.body.style.position = "fixed";
      document.body.style.top = `-${lockedScrollY}px`;
      document.body.style.left = "0";
      document.body.style.right = "0";
      document.body.style.width = "100%";
    } else {
      scrollLockMode = "overflow";
    }
    document.body.style.overflow = "hidden";
  }

  scrollLockDepth += 1;
}

function unlockPageScroll() {
  if (scrollLockDepth === 0) return;

  scrollLockDepth -= 1;
  if (scrollLockDepth > 0) return;

  const shouldRestorePosition = scrollLockMode === "fixed";
  if (shouldRestorePosition) {
    document.body.style.position = "";
    document.body.style.top = "";
    document.body.style.left = "";
    document.body.style.right = "";
    document.body.style.width = "";
  }
  document.body.style.overflow = "";
  document.documentElement.classList.remove("is-scroll-locked");
  if (shouldRestorePosition) window.scrollTo(0, lockedScrollY);
  scrollLockMode = null;
}

function trapFocus(event, container) {
  if (event.key !== "Tab") return;

  const focusable = [...container.querySelectorAll("a[href], button:not([disabled]), [tabindex]:not([tabindex='-1'])")].filter(
    (node) => node.getClientRects().length && node.getAttribute("aria-hidden") !== "true"
  );
  if (!focusable.length) return;

  const first = focusable[0];
  const last = focusable[focusable.length - 1];
  if (event.shiftKey && document.activeElement === first) {
    event.preventDefault();
    last.focus();
  } else if (!event.shiftKey && document.activeElement === last) {
    event.preventDefault();
    first.focus();
  }
}

