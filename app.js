function initPageEntrance() {
  const root = document.documentElement;
  const reveal = () => {
    root.classList.remove("is-page-loading");
    root.classList.add("is-page-entered");
  };

  if (window.matchMedia?.("(prefers-reduced-motion: reduce)").matches) {
    reveal();
    return;
  }

  requestAnimationFrame(reveal);
}

function initSectionEntrances() {
  if (sectionEntranceObserver) {
    sectionEntranceObserver.disconnect();
    sectionEntranceObserver = null;
  }

  const sections = [...document.querySelectorAll(".scroll-reveal")];
  if (!sections.length) return;

  const reveal = (section) => section.classList.add("is-revealed");
  const reduceMotion = window.matchMedia?.("(prefers-reduced-motion: reduce)").matches;

  sections.forEach((section) => {
    section.classList.add("is-reveal-pending");
    if (reduceMotion || typeof IntersectionObserver === "undefined") reveal(section);
  });

  if (reduceMotion || typeof IntersectionObserver === "undefined") return;

  sectionEntranceObserver = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) return;
        reveal(entry.target);
        sectionEntranceObserver.unobserve(entry.target);
      });
    },
    { threshold: 0.12, rootMargin: "0px 0px -7% 0px" }
  );

  sections.forEach((section) => sectionEntranceObserver.observe(section));
}

function initSplitText() {
  if (splitTextObserver) {
    splitTextObserver.disconnect();
    splitTextObserver = null;
  }

  const activateSplitText = (node) => {
    if (node.classList.contains("is-split-ready")) return;

    requestAnimationFrame(() => node.classList.add("is-split-ready"));

    const letters = node.querySelectorAll(".split-letter");
    const delay = Number(node.dataset.splitDelay || 50);
    const duration = Number(node.dataset.splitDuration || 1250);
    const totalDuration = duration + Math.max(letters.length - 1, 0) * delay;

    window.setTimeout(() => {
      node.dispatchEvent(new CustomEvent("letteranimationcomplete", { bubbles: true }));
    }, totalDuration);
  };

  const splitNodes = [...document.querySelectorAll("[data-split-text]")];

  splitNodes.forEach((node) => {
    node.classList.remove("is-split-ready");
    const value = node.textContent || "";
    const delay = Number(node.dataset.splitDelay || 50);
    const duration = Number(node.dataset.splitDuration || 1250);
    node.setAttribute("aria-label", value);
    node.replaceChildren();

    [...value].forEach((character, index) => {
      const letter = document.createElement("span");
      letter.className = "split-letter";
      letter.setAttribute("aria-hidden", "true");
      letter.textContent = character === " " ? "\u00a0" : character;
      letter.style.setProperty("--letter-delay", `${index * delay}ms`);
      letter.style.setProperty("--letter-duration", `${duration}ms`);
      node.append(letter);
    });
  });

  if ("IntersectionObserver" in window) {
    splitTextObserver = new IntersectionObserver(
      (entries) => {
        entries.forEach((entry) => {
          if (!entry.isIntersecting) return;
          activateSplitText(entry.target);
          splitTextObserver.unobserve(entry.target);
        });
      },
      { threshold: 0.1, rootMargin: "-100px" }
    );
    splitNodes.forEach((node) => splitTextObserver.observe(node));
  } else {
    splitNodes.forEach(activateSplitText);
  }
}


