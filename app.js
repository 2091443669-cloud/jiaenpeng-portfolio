class CertificateGallery {
  constructor(root, items, { bend = 1, scrollSpeed = 2, scrollEase = 0.05 } = {}) {
    this.root = root;
    this.items = items;
    this.isMobileLayout = isMobileViewport();
    this.bend = bend;
    this.scrollSpeed = scrollSpeed;
    this.scrollEase = this.isMobileLayout ? Math.max(scrollEase, 0.14) : scrollEase;
    this.scroll = { current: 0, target: 0 };
    this.pointer = null;
    this.touch = null;
    this.touchMomentum = 0;
    this.touchReleaseFollow = 0;
    this.suppressClick = false;
    this.suppressClickUntil = 0;
    this.gestureLockThreshold = this.isMobileLayout ? 4 : 8;
    this.gestureLockRatio = this.isMobileLayout ? 0.68 : 1.1;
    this.autoSpeed = this.isMobileLayout ? 0.018 : 0.022;
    this.autoResumeDelay = this.isMobileLayout ? 4400 : 3000;
    this.lastInteraction = performance.now();
    this.lastFrameTime = performance.now();
    this.lastPaintTime = 0;
    this.frameInterval = 0;
    this.isVisible = typeof IntersectionObserver === "undefined";
    this.raf = null;
    this.motionQuery = window.matchMedia?.("(prefers-reduced-motion: reduce)");
    this.prefersReducedMotion = Boolean(this.motionQuery?.matches);

    this.track = document.createElement("div");
    this.track.className = "certificate-gallery-track";
    this.root.appendChild(this.track);
    this.cards = this.items.map((item, index) => this.createCard(item, index));

    this.onResize = this.onResize.bind(this);
    this.onPointerDown = this.onPointerDown.bind(this);
    this.onPointerMove = this.onPointerMove.bind(this);
    this.onPointerUp = this.onPointerUp.bind(this);
    this.onLostPointerCapture = this.onLostPointerCapture.bind(this);
    this.onTouchStart = this.onTouchStart.bind(this);
    this.onTouchMove = this.onTouchMove.bind(this);
    this.onTouchEnd = this.onTouchEnd.bind(this);
    this.onSelectStart = this.onSelectStart.bind(this);
    this.onDragStart = this.onDragStart.bind(this);
    this.onKeyDown = this.onKeyDown.bind(this);
    this.onIntersection = this.onIntersection.bind(this);
    this.onVisibilityChange = this.onVisibilityChange.bind(this);
    this.onMotionPreferenceChange = this.onMotionPreferenceChange.bind(this);

    this.root.addEventListener("pointerdown", this.onPointerDown);
    this.root.addEventListener("pointermove", this.onPointerMove);
    this.root.addEventListener("pointerup", this.onPointerUp);
    this.root.addEventListener("pointercancel", this.onLostPointerCapture);
    this.root.addEventListener("lostpointercapture", this.onLostPointerCapture);
    this.root.addEventListener("touchstart", this.onTouchStart, { passive: true });
    this.root.addEventListener("touchmove", this.onTouchMove, { passive: false });
    this.root.addEventListener("touchend", this.onTouchEnd, { passive: false });
    this.root.addEventListener("touchcancel", this.onTouchEnd, { passive: false });
    this.root.addEventListener("selectstart", this.onSelectStart);
    this.root.addEventListener("dragstart", this.onDragStart);
    this.root.addEventListener("keydown", this.onKeyDown);
    window.addEventListener("resize", this.onResize);
    window.addEventListener("pointerup", this.onPointerUp);
    window.addEventListener("blur", this.onLostPointerCapture);
    document.addEventListener("visibilitychange", this.onVisibilityChange);
    this.motionQuery?.addEventListener?.("change", this.onMotionPreferenceChange);

    this.intersectionObserver = typeof IntersectionObserver === "undefined"
      ? null
      : new IntersectionObserver(this.onIntersection, { threshold: 0.08 });
    this.intersectionObserver?.observe(this.root);

    this.onResize();
    this.setLanguage();
    this.render(performance.now(), false);
    this.start();
  }

  createCard(item, index) {
    const card = document.createElement("button");
    card.className = "certificate-gallery-card";
    card.type = "button";
    card.dataset.certificateIndex = `${index}`;
    card.innerHTML = `
      <span class="certificate-gallery-media"><img src="${item.image}" alt="" loading="lazy" draggable="false" /></span>
      <span class="certificate-gallery-copy"><span>${item.year}</span><strong></strong><small></small></span>
    `;
    card.addEventListener("click", (event) => {
      if (this.suppressClick || performance.now() < this.suppressClickUntil) {
        event.preventDefault();
        return;
      }
      this.openCard(index);
    });
    this.track.appendChild(card);
    return { item, element: card };
  }

  openCard(index) {
    const item = this.items[index];
    if (!item) return;

    const galleryItems = this.items.map((entry) => ({
      src: entry.image,
      caption: `${entry.year} · ${t(entry.titleKey)} · ${t(entry.prizeKey)}`
    }));
    openImageViewer(item.image, galleryItems[index].caption, galleryItems, index);
  }

  releaseTouchMomentum(velocity) {
    const speed = Math.abs(velocity);
    if (speed < 0.025) return 0;
    return Math.sign(velocity) * Math.min(2.4, speed);
  }

  setLanguage() {
    this.cards.forEach(({ item, element }) => {
      const image = element.querySelector("img");
      const title = element.querySelector("strong");
      const prize = element.querySelector("small");
      const text = `${item.year} · ${t(item.titleKey)}`;
      image.alt = text;
      title.textContent = t(item.titleKey);
      prize.textContent = t(item.prizeKey);
    });
  }

  onResize() {
    const rect = this.root.getBoundingClientRect();
    this.width = Math.max(1, rect.width);
    this.height = Math.max(1, rect.height);
    const maxCardHeight = Math.max(210, this.height - 14);
    this.cardWidth = Math.min(420, Math.max(180, Math.min(this.width * 0.36, maxCardHeight / Math.SQRT2)));
    this.cardHeight = Math.min(maxCardHeight, this.cardWidth * Math.SQRT2);
    this.cardTop = Math.max(7, (this.height - this.cardHeight) / 2);
    this.spacing = this.cardWidth + Math.max(this.isMobileLayout ? 18 : 28, this.cardWidth * (this.isMobileLayout ? 0.12 : 0.16));
    this.totalWidth = this.spacing * this.cards.length;
    this.cards.forEach(({ element }) => {
      element.style.width = `${this.cardWidth}px`;
      element.style.height = `${this.cardHeight}px`;
    });
    this.render(performance.now(), false);
  }

  onIntersection(entries) {
    this.isVisible = Boolean(entries[0]?.isIntersecting);
    if (!this.isVisible) return;
    this.render(performance.now(), false);
    this.start();
  }

  onVisibilityChange() {
    if (!document.hidden) this.start();
  }

  onMotionPreferenceChange(event) {
    this.prefersReducedMotion = event.matches;
    this.touchMomentum = 0;
    this.touchReleaseFollow = 0;
    if (this.prefersReducedMotion) {
      if (this.raf) window.cancelAnimationFrame(this.raf);
      this.raf = null;
      this.render(performance.now(), false);
      return;
    }
    this.start();
  }

  onTouchStart(event) {
    if (!this.isMobileLayout || event.touches.length !== 1) return;
    const touch = event.touches[0];
    this.pointer = null;
    this.touchMomentum = 0;
    this.touchReleaseFollow = 0;
    const now = performance.now();
    this.touch = {
      startX: touch.clientX,
      startY: touch.clientY,
      lastX: touch.clientX,
      lastTime: now,
      startTarget: this.scroll.target,
      velocity: 0,
      releaseVelocity: 0,
      gesture: "pending",
      moved: false,
      cardIndex: Number(event.target.closest(".certificate-gallery-card")?.dataset.certificateIndex)
    };
    this.markInteraction();
  }

  onTouchMove(event) {
    if (!this.touch || event.touches.length !== 1) return;
    const touch = event.touches[0];
    const distance = this.touch.startX - touch.clientX;
    const verticalDistance = touch.clientY - this.touch.startY;
    const absX = Math.abs(distance);
    const absY = Math.abs(verticalDistance);

    if (Math.hypot(distance, verticalDistance) > 5) this.touch.moved = true;

    if (this.touch.gesture === "pending" && Math.max(absX, absY) > 2) {
      if (absY > absX * 1.35) {
        this.touch.gesture = "scroll";
        this.suppressClick = true;
        this.suppressClickUntil = performance.now() + 650;
        return;
      }
      this.touch.gesture = "drag";
    }

    if (this.touch.gesture !== "drag") return;

    if (event.cancelable) event.preventDefault();
    this.markInteraction();
    const now = performance.now();
    const deltaTime = Math.max(16, now - this.touch.lastTime);
    const touchDragSpeed = 0.82;
    const stepVelocity = ((this.touch.lastX - touch.clientX) * this.scrollSpeed * touchDragSpeed) / deltaTime;
    this.touch.velocity = this.touch.velocity * 0.76 + stepVelocity * 0.24;
    this.touch.releaseVelocity = this.touch.velocity * 0.45 + stepVelocity * 0.55;
    this.touch.lastX = touch.clientX;
    this.touch.lastTime = now;
    this.scroll.target = this.touch.startTarget + distance * this.scrollSpeed * touchDragSpeed;
    this.scroll.current += (this.scroll.target - this.scroll.current) * 0.5;
    this.render(performance.now(), false);
  }

  onTouchEnd(event) {
    if (!this.touch || event.touches.length) return;
    const { moved, cardIndex, gesture, velocity, releaseVelocity } = this.touch;
    this.touch = null;
    if (moved && gesture === "drag") {
      this.touchMomentum = this.releaseTouchMomentum(releaseVelocity || velocity);
      this.touchReleaseFollow = this.touchMomentum ? 1 : 0;
      this.start();
    }

    if (!moved && gesture !== "scroll" && Number.isInteger(cardIndex)) {
      this.suppressClick = true;
      this.openCard(cardIndex);
    } else {
      this.suppressClick = moved;
      if (moved) this.suppressClickUntil = performance.now() + 650;
    }

    window.setTimeout(() => {
      this.suppressClick = false;
    }, moved ? 220 : 0);
  }

  onPointerDown(event) {
    if (event.pointerType === "touch" && this.isMobileLayout) return;
    if (event.button !== 0) return;
    // Prevent a long drag from turning certificate labels or images into a blue
    // browser selection while preserving the gallery's click and key controls.
    const isTouchMobile = event.pointerType === "touch" && isMobileViewport();
    if (!isTouchMobile && event.cancelable) event.preventDefault();
    this.markInteraction();
    this.pointer = {
      id: event.pointerId,
      startX: event.clientX,
      startY: event.clientY,
      startTarget: this.scroll.target,
      gesture: isTouchMobile ? "pending" : "drag",
      pointerType: event.pointerType,
      moved: false,
      cardIndex: Number(event.target.closest(".certificate-gallery-card")?.dataset.certificateIndex)
    };
    if (!isTouchMobile) this.root.setPointerCapture?.(event.pointerId);
  }

  onPointerMove(event) {
    if (event.pointerType === "touch" && this.isMobileLayout) return;
    if (!this.pointer || event.pointerId !== this.pointer.id) return;
    if (event.pointerType === "mouse" && (event.buttons & 1) === 0) {
      this.onLostPointerCapture();
      return;
    }

    const distance = this.pointer.startX - event.clientX;
    const verticalDistance = event.clientY - this.pointer.startY;
    if (Math.hypot(distance, verticalDistance) > 10) this.pointer.moved = true;
    if (this.pointer.pointerType === "touch" && isMobileViewport()) {
      const absX = Math.abs(distance);
      const absY = Math.abs(verticalDistance);

      if (this.pointer.gesture === "pending" && Math.max(absX, absY) > this.gestureLockThreshold) {
        if (absX > absY * this.gestureLockRatio) {
          this.pointer.gesture = "drag";
          this.pointer.moved = true;
          this.root.setPointerCapture?.(event.pointerId);
        } else if (absY > absX * this.gestureLockRatio) {
          this.pointer.gesture = "scroll";
          this.pointer.moved = true;
          this.suppressClick = true;
          this.suppressClickUntil = performance.now() + 800;
          return;
        }
      }

      if (this.pointer.gesture === "scroll" || this.pointer.gesture === "pending") return;
    }

    if (event.cancelable) event.preventDefault();
    this.markInteraction();
    const dragSpeed = this.isMobileLayout ? 1.18 : 0.7;
    this.scroll.target = this.pointer.startTarget + distance * this.scrollSpeed * dragSpeed;
  }

  onPointerUp(event) {
    if (event.pointerType === "touch" && this.isMobileLayout) return;
    if (!this.pointer || event.pointerId !== this.pointer.id) return;
    const totalX = Math.abs(event.clientX - this.pointer.startX);
    const totalY = Math.abs(event.clientY - this.pointer.startY);
    const isVerticalDrag = totalY > 12 && totalY > totalX;
    const moved = this.pointer.moved || isVerticalDrag;
    const { cardIndex, gesture } = this.pointer;
    this.pointer = null;

    if (!moved && gesture !== "scroll" && Number.isInteger(cardIndex)) {
      // Pointer capture can prevent a native click from reaching the button.
      // Open directly on release, then ignore that duplicate native click.
      this.suppressClick = true;
      this.openCard(cardIndex);
    } else {
      this.suppressClick = moved;
      if (moved) this.suppressClickUntil = performance.now() + 800;
    }

    window.setTimeout(() => {
      this.suppressClick = false;
    }, moved ? 260 : 0);
  }

	  onLostPointerCapture() {
    const shouldSuppressClick = Boolean(this.pointer?.moved);
    this.pointer = null;
    this.touchMomentum = 0;
    this.touchReleaseFollow = 0;
	    if (shouldSuppressClick) {
	      this.suppressClick = true;
      this.suppressClickUntil = performance.now() + 800;
      window.setTimeout(() => {
        this.suppressClick = false;
      }, 800);
    }
  }

  onSelectStart(event) {
    event.preventDefault();
  }

  onDragStart(event) {
    event.preventDefault();
  }

  onKeyDown(event) {
    this.markInteraction();
    if (event.key === "ArrowRight") {
      event.preventDefault();
      this.scroll.target += this.spacing;
    }
    if (event.key === "ArrowLeft") {
      event.preventDefault();
      this.scroll.target -= this.spacing;
    }
    if (event.key === "Home") {
      event.preventDefault();
      this.scroll.target = 0;
    }
  }

  markInteraction() {
    this.lastInteraction = performance.now();
  }

  start() {
    if (this.raf || !this.isVisible || document.hidden || this.prefersReducedMotion) return;
    this.raf = window.requestAnimationFrame((timestamp) => this.frame(timestamp));
  }

  render(timestamp, shouldAutoScroll) {
    const deltaTime = Math.min(64, Math.max(0, timestamp - this.lastFrameTime));
    this.lastFrameTime = timestamp;
    if (!this.pointer && !this.touch && Math.abs(this.touchMomentum) > 0.01) {
      this.scroll.target += this.touchMomentum * deltaTime;
      const drag = 0.0048 + Math.min(Math.abs(this.touchMomentum) * 0.00045, 0.0012);
      this.touchMomentum *= Math.exp(-deltaTime * drag);
      if (Math.abs(this.touchMomentum) < 0.01) this.touchMomentum = 0;
    }
    if (!this.pointer && !this.touch && this.touchReleaseFollow > 0) {
      this.touchReleaseFollow *= Math.exp(-deltaTime * 0.01);
      if (this.touchReleaseFollow < 0.001) this.touchReleaseFollow = 0;
    }
    if (shouldAutoScroll && !this.pointer && !this.touch && !this.touchMomentum && !this.touchReleaseFollow && timestamp - this.lastInteraction > this.autoResumeDelay) {
      this.scroll.target += deltaTime * this.autoSpeed;
    }
    const releaseEase = this.scrollEase + (0.5 - this.scrollEase) * this.touchReleaseFollow;
    this.scroll.current += (this.scroll.target - this.scroll.current) * releaseEase;
    const halfViewport = this.width / 2 + this.cardWidth * 0.45;

    this.cards.forEach(({ element }, index) => {
      let x = index * this.spacing - this.scroll.current;
      x = ((x + this.totalWidth / 2) % this.totalWidth + this.totalWidth) % this.totalWidth - this.totalWidth / 2;
      const ratio = Math.min(1.15, Math.abs(x) / halfViewport);
      const y = ratio * ratio * this.height * 0.16 * this.bend;
      const rotation = -(x / halfViewport) * 8 * this.bend;
      const scale = 1 - ratio * 0.12;
      const opacity = Math.max(0.24, 1 - ratio * 0.48);

      element.style.opacity = `${opacity}`;
      element.style.zIndex = `${Math.round(1000 - ratio * 100)}`;
      element.style.transform = `translate3d(${x - this.cardWidth / 2}px, ${this.cardTop + y}px, 0) rotateZ(${rotation}deg) scale(${scale})`;
    });
  }

  frame(timestamp) {
    this.raf = null;
    if (!this.isVisible || document.hidden || this.prefersReducedMotion) return;
    if (this.frameInterval && timestamp - this.lastPaintTime < this.frameInterval) {
      this.start();
      return;
    }
    this.lastPaintTime = timestamp;
    this.render(timestamp, true);
    this.start();
  }
}

